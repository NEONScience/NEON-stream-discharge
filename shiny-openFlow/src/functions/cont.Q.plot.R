#' Plot NEON Hydrology Data Using Outputs from neonStageQPlot::get.cont.Q.NEON.API

#' @name cont.Q.plot

#' @author
#' Zachary L. Nickerson \email{nickerson@battelleecology.org} \cr
#' James Ross \email{ross.james94@gmail.com} \cr

#' @description  This function will generate a plotly plot of NEON hydrology data to be
#' rendered in the openFlow shiny app. The function can also be used outside the app, but should
#' only be used with the outputs from neonStageQPlot::get.cont.Q.NEON.API.

#' @param site.id Required: NEON AQU site ID selected by the shiny app user [string]
#' @param start.date Required: Search interval start date (YYYY-MM-DD) selected by the shiny
#' app user [string]
#' @param end.date Required: Search interval end date (YYYY-MM-DD) selected by the shiny app
#' user [string]
#' @param plot.imp.unit Defaults to FALSE: Idicator of plotting data in metric or imperial
#' units [boolean]
#' @param plot.sci.rvw.QF Dafults to FALSE: Indicator of plotting the finalDischargeQFSciRvw
#' field from the DP4.00130.001 data product [boolean]
#' @param plot.q.stats Defaults to FALSE: Include values for 3x median discharge and 25-75%
#' flow in the plot. Statistics are calculated from the time range selected by the user and
#' exclude records that contain a science review quality flag (dischargeFinalQFSciRvw) [boolean]
#' @param mode.dark Defaults to FALSE: Indicator of plotting data in light or dark mode. NOTE:
#' This argument is only used in the openFlow shiny app. [boolean]

#' @return Returns a plotly plot object

#' @references
#' License: GNU AFFERO GENERAL PUBLIC LICENSE Version 3, 19 November 2007

#' @importFrom dplyr %>%

#' @export cont.Q.plot

# changelog and author contributions / copyrights
#   Zachary L. Nickerson (2022-07-25)
#     original creation
#   Zachary L. Nickerson (2022-08-09)
#     removed dependency on measurements
#   Zachary L. Nickerson (2023-02-13)
#     updates to include 3x median discharge
#   Zachary L. Nickerson (2023-02-13)
#     updates to add clarity to the identification and interpretation of 3x median discharge
##############################################################################################
base::options(stringsAsFactors = F)
utils::globalVariables(c('histMedQ','meanURemnUnc','meanLRemnUnc','meanUParaUnc','meanLParaUnc','meanQ','streamDischarge','meanUHUnc','meanLHUnc','meanH','gaugeHeight','gauge_Height','priPrecipBulkLoUnc','priPrecipBulkUpUnc','priPrecipBulk','secPrecipBulkLoUnc','secPrecipBulkUpUnc','secPrecipBulk'))

cont.Q.plot <-function(site.id,
                       start.date,
                       end.date,
                       # input.list,
                       plot.imp.unit=F,
                       mode.dark=F,
                       # plot.final.QF=F,
                       plot.sci.rvw.QF=F,
                       # plot.precip.final.QF=F,
                       # plot.precip.sci.rvw.QF=F,
                       plot.q.stats=F
                       ){
  
  if(base::missing(site.id)){
    stop('must provide site.id for plotting continuous discharge')
  }
  if(base::missing(start.date)){
    stop('must provide start.date for plotting continuous discharge')
  }
  if(base::missing(end.date)){
    stop('must provide end.date for plotting continuous discharge')
  }
  # if(base::missing(input.list)){
  #   stop('must provide input.list for plotting continuous discharge')
  # }
  
  # Connect to openflow database
  con<-DBI::dbConnect(
    RPostgres::Postgres(),
    dbname = 'openflow',
    host = 'nonprod-commondb.gcp.neoninternal.org',
    port = '5432',
    user = 'shiny_openflow_rw',
    password = Sys.getenv('DB_TOKEN')
  )
  
  # Read in metadata table from openflow database 
  productList <- readr::read_csv(base::url("https://raw.githubusercontent.com/NEONScience/NEON-stream-discharge/main/shiny-openFlow/aqu_dischargeDomainSiteList.csv"))
  productList <- DBI::dbReadTable(con,"sitelist")
  
  # Get data
  startDateFormat <- format(as.POSIXct(start.date),"%Y-%m-%d 00:00:00")
  endDateFormat <- format(as.POSIXct(end.date)+86400,"%Y-%m-%d 00:00:00")
  dbquery <- sprintf("SELECT * FROM contqsum WHERE \"siteID\" = '%s' AND \"date\" > timestamp '%s' AND \"date\" < timestamp '%s'",site.id,startDateFormat,endDateFormat)
  contqsum <- DBI::dbSendQuery(con,dbquery)
  contqsum <- DBI::dbFetch(contqsum)
  if(sum(!is.na(contqsum$priPrecipBulk))){
    isPrimaryPtp <- T
  }else{
    isPrimaryPtp <- F
  }
  precipSiteID <- productList$ptpSite[productList$siteID==site.id]
  dbquery <- sprintf("SELECT * FROM histmedq WHERE \"siteID\" = '%s'",site.id)
  histmedq <- DBI::dbSendQuery(con,dbquery)
  histmedq <- DBI::dbFetch(histmedq)
  contqsum$monthDay <- format(contqsum$date,"%m-%d %H:%M:%00")
  contqsum <- dplyr::left_join(contqsum,histmedq,by="monthDay")
  histMedQMinYear <- unique(contqsum$minYear)
  histMedQMaxYear <- unique(contqsum$maxYear)
  continuousDischarge_sum <- contqsum
  
  #3x internal dataGet#
  if(plot.q.stats){
    medQ <- productList$threeXMedQ[productList$siteID==site.id]
    medQPlusUnc <- productList$threeXMedQPlusUnc[productList$siteID==site.id]
    medQUnc <- productList$threeXMedQUnc[productList$siteID==site.id]
    usgsProxy <- productList$usgsProxy[productList$siteID==site.id]
  }
  
  #axis units
  y1Units <- "(liters per second)"
  y2Units <- "(meter)"
  y3Units <- "(milimeter)"
  convLPStoCFS <- 0.035314
  convMtoFt <- 3.28084
  convMMtoIN <- 0.0393701
  
  #SI to imperial
  ##needs to be above plotly call so axis are created correctly
  if(plot.imp.unit){
    continuousDischarge_sum <- continuousDischarge_sum %>%
      dplyr::mutate(#Discharge
                    histMedQ = histMedQ*convLPStoCFS,
                    meanURemnUnc = meanURemnUnc*convLPStoCFS,
                    meanLRemnUnc = meanLRemnUnc*convLPStoCFS,
                    meanUParaUnc = meanUParaUnc*convLPStoCFS,
                    meanLParaUnc = meanLParaUnc*convLPStoCFS,
                    meanQ = meanQ*convLPStoCFS,
                    streamDischarge = streamDischarge*convLPStoCFS,
                    dischargeFinalQFSciRvw = dischargeFinalQFSciRvw*convLPStoCFS,
                    #Stage
                    meanUHUnc = meanUHUnc*convMtoFt,
                    meanLHUnc = meanLHUnc*convMtoFt,
                    meanH = meanH*convMtoFt,
                    gaugeHeight = gaugeHeight*convMtoFt,
                    gauge_Height = gauge_Height*convMtoFt)

    #Precipitation
    if(!base::is.null(isPrimaryPtp)){
      if(isPrimaryPtp){
        continuousDischarge_sum <- continuousDischarge_sum %>%
          dplyr::mutate(priPrecipBulkLoUnc = priPrecipBulkLoUnc*convMMtoIN,
                        priPrecipBulkUpUnc = priPrecipBulkUpUnc*convMMtoIN,
                        priPrecipBulk = priPrecipBulk*convMMtoIN)
      }else{
        continuousDischarge_sum <- continuousDischarge_sum %>%
          dplyr::mutate(secPrecipBulkLoUnc = secPrecipBulkLoUnc*convMMtoIN,
                        secPrecipBulkUpUnc = secPrecipBulkUpUnc*convMMtoIN,
                        secPrecipBulk = secPrecipBulk*convMMtoIN)
      }
    }
    
    #3x internal
    if(plot.q.stats){
      medQ <- medQ*convLPStoCFS
      medQPlusUnc <- medQPlusUnc*convLPStoCFS
      medQUnc <- medQUnc*convLPStoCFS
    }
    
    y1Units <- "(Cubic Feet per second)"
    y2Units <- "(Feet)"
    y3Units <- "(Inches)"
  }
  
  #y-axis
  y1 <- base::list(side='left',
                   automargin=T,
                   title=stringr::str_c("Discharge ",y1Units),
                   tickfont=base::list(size=16),
                   titlefont=base::list(size=18),
                   showgrid=F,
                   zeroline=F)
  
  y2 <- base::list(side='right',
                   overlaying="y",
                   automargin=T,
                   title=stringr::str_c("Stage ",y2Units),
                   tickfont=base::list(size=16,color = '#CC79A7'),
                   titlefont=base::list(size=18,color = '#CC79A7'),
                   showgrid=F,
                   zeroline=F)
  
  y3 <- base::list(side='right',
                   overlaying="y",
                   automargin=T,
                   title=stringr::str_c("Percipitation ",y3Units),
                   tickfont=base::list(size=16,color = "#0072B2"),
                   titlefont=base::list(size=18,color = "#0072B2"),
                   showgrid=F,
                   zeroline=F,
                   anchor="free",
                   position=0.98)
  
  # Build plot layout
  method <- plotly::plot_ly(data=continuousDischarge_sum, source = "phenoDate")%>%
    plotly::layout(
      yaxis = y1, yaxis2 = y2, yaxis3 = y3,
      xaxis=base::list(domain=c(0,.9),
                       tick=14,
                       automargin=T,
                       title="Date",
                       tickfont=base::list(size=16),
                       titlefont=base::list(size=18)),
      legend=base::list(x=-0.2,y=0.87,
                        font=base::list(size=14)),
      updatemenus=base::list(
        base::list(
          type='buttons',
          showactive=FALSE,
          buttons=base::list(
            base::list(label='Scale Discharge\n- Linear -',
                       method='relayout',
                       args=base::list(base::list(yaxis=base::list(type='linear',
                                                                   title=stringr::str_c("Discharge ",y1Units),
                                                                   tickfont=base::list(size=16),
                                                                   titlefont=base::list(size=18),
                                                                   showgrid=F,
                                                                   zeroline=F)))),
            base::list(label='Scale Discharge\n- Log -',
                       method='relayout',
                       args=base::list(base::list(yaxis=base::list(type='log',
                                                                   title=stringr::str_c("Discharge ",y1Units," - log"),
                                                                   tickfont=base::list(size=16),
                                                                   titlefont=base::list(size=18),
                                                                   showgrid=F,
                                                                   zeroline=F))))))))
  
  
  #dark mode styling
  dischargeColor <- "black"
  if(mode.dark){
    method <- method %>%
      plotly::layout(paper_bgcolor="#222",plot_bgcolor='#222',
                     font = base::list(color = 'white'))
    dischargeColor <- "white"
  }
  
  # Add Quality flags
  # if(plot.final.QF){
  #   method <- method %>%
  #     plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~dischargeFinalQF,type='scatter',mode='none',fill = 'tozeroy',showlegend= F, hoverinfo="none", fillcolor = 'lightgray')
  # }
  if(plot.sci.rvw.QF){
    method <- method %>%
      plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~dischargeFinalQFSciRvw,type='scatter',mode='none',fill = 'tozeroy',hoverinfo="none", showlegend= F, fillcolor = 'lightgray')
  }
  # #wraps precip flag data to prevent plotting when data does not exist
  # if(isPrimaryPtp & plot.precip.final.QF){
  #   method <- method %>%
  #     plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~priPrecipFinalQF,type='scatter',mode='none',fill = 'tozeroy',showlegend= F, hoverinfo="none", fillcolor = 'gray')
  # }
  #
  # #wraps precip flag data to prevent plotting when data does not exist
  # if(isPrimaryPtp==FALSE & plot.precip.sci.rvw.QF){
  #   method <- method %>%
  #     plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~secPrecipSciRvwQF,type='scatter',mode='none',fill = 'tozeroy',hoverinfo="none", showlegend= F, fillcolor = 'gray')
  # }
  
  # Add base plot
  method <- method %>%
    
    # Q Uncertainty
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~meanURemnUnc,name="Discharge\nRemnant\nUncertainty",type='scatter',mode='line',line=base::list(color='#D55E00'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=F,legendgroup='group1',visible = "legendonly")%>%
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~meanLRemnUnc,name="Discharge\nRemnant\nUncertainty",type='scatter',mode='none',fill = 'tonexty',fillcolor = '#D55E00',hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=T,legendgroup='group1',visible = "legendonly")%>%
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~meanUParaUnc,name="Discharge\nParametric\nUncertainty",type='scatter',mode='line',line=base::list(color='#E69F00'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=F,legendgroup='group2',visible = "legendonly")%>%
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~meanLParaUnc,name="Discharge\nParametric\nUncertainty",type='scatter',mode='none',fill = 'tonexty',fillcolor = '#E69F00',hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=T,legendgroup='group2',visible = "legendonly")%>%
    
    # H Uncertainty
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~meanUHUnc,name="Stage\nUncertainty",type='scatter',mode='line',line=base::list(color='#56B4E9'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",yaxis='y2',showlegend=F,legendgroup='group3',visible = "legendonly")%>%
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~meanLHUnc,name="Stage\nUncertainty",type='scatter',mode='none',fill = 'tonexty',fillcolor = '#56B4E9',hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",yaxis='y2',showlegend=T,legendgroup='group3',visible = "legendonly")%>%
    
    # H and Q Series
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~meanQ, name="Continuous\nDischarge",type='scatter',mode='lines',line = base::list(color = dischargeColor),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",yaxis='y1',legendgroup='group4')%>%
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~meanH, name="Continuous\nStage",type='scatter',mode='lines',line = base::list(color = '#CC79A7'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",yaxis='y2',showlegend=T,legendgroup='group5')%>%
    
    # Empirical H and Q
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~streamDischarge,name="Measured\nDischarge", type='scatter', mode='markers',marker = base::list(color = '#009E73',size=8,line = base::list(color = "black",width = 1)),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=T,legendgroup='group6')%>%
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~gaugeHeight,name='Measured\nGauge\nHeight',type='scatter',mode='markers',yaxis='y2',marker=base::list(color="#F0E442",size=8,line = base::list(color = "black",width = 1)),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=F,legendgroup='group7')%>%
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~gauge_height,name='Measured\nGauge\nHeight',type='scatter',mode='markers',yaxis='y2',marker=base::list(color="#F0E442",size=8,line = base::list(color = "black",width = 1)),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=T,legendgroup='group7')%>%
    
    #Historical Med Q
    plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~histMedQ, name=stringr::str_c("Historic Median\nDischarge: ","\n",base::min(histMedQMinYear),"-",base::max(histMedQMaxYear)),type='scatter',mode='lines',line = base::list(color = 'grey'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",legendgroup='group8',visible = "legendonly")
  
  #Precipitation Data
  #plots whichever data is available using isPrimaryPtp bool
  if(!base::is.null(isPrimaryPtp)){
    if(isPrimaryPtp){
      method <- method %>%
        plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~priPrecipBulkLoUnc,name="Continuous\nPrecipitation\nUncertainty",yaxis = "y3",type='scatter',mode='line',line=base::list(color='#431A74'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=F,legendgroup='group9',visible = "legendonly")%>%
        plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~priPrecipBulkUpUnc,name="Continuous\nPrecipitation\nUncertainty",yaxis = "y3",type='scatter',mode='none',fill = 'tonexty',fillcolor = '#431A74',hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=T,legendgroup='group9',visible = "legendonly") %>%
        plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~priPrecipBulk,name=stringr::str_c("Continuous\nPrecipitation\nSite: ",precipSiteID),yaxis = "y3",type='scatter',mode='lines',line = base::list(color = '#0072B2'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",legendgroup='group10',visible = "legendonly")
    }else{
      method <- method %>%
        plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~secPrecipBulkLoUnc,name="Continuous\nPrecipitation\nUncertainty",yaxis = "y3",type='scatter',mode='line',line=base::list(color='#431A74'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=F,legendgroup='group9',visible = "legendonly")%>%
        plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~secPrecipBulkUpUnc,name="Continuous\nPrecipitation\nUncertainty",yaxis = "y3",type='scatter',mode='none',fill = 'tonexty',fillcolor = '#431A74',hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",showlegend=T,legendgroup='group9',visible = "legendonly") %>%
        plotly::add_trace(x=~base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),y=~secPrecipBulk,name=stringr::str_c("Continuous\nPrecipitation\nSite: ",precipSiteID),yaxis = "y3",type='scatter',mode='lines',line = base::list(color = '#0072B2'),hovertemplate = "Date/UTC-Time: %{x}<br>Value: %{y}<br>Click to view image of stream",legendgroup='group10',visible = "legendonly")
    }
  }
  
  # Add the internal parameters
  if(plot.q.stats){
    if(!base::is.na(medQPlusUnc)){
      method <- method%>%
        plotly::add_segments(x=~base::min(base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),na.rm = T),xend=~base::max(base::as.POSIXct(date,format="%Y-%m-%d %H:%M:%S"),na.rm = T),y=~medQPlusUnc,yend=~medQPlusUnc,line=base::list(color='grey',dash='dash'),name="3x Median\nDischarge\n+ Uncertainty",showlegend=T,legendgroup='group11',visible = "legendonly",hoverinfo="none")
      
      if(!plot.imp.unit){
        method <- method%>%
          plotly::layout(
            title=list(text=base::paste0("<br><b>3x Median Discharge (w/ Uncertainty) = ",base::round(medQ,digits = 0)," + ",base::round(medQUnc,digits = 1)," = ",base::round(medQPlusUnc,digits = 0)," L/s<br>Corresponds to the dashed line plotted on the primary y-axis (left-hand; Discharge)."),
                       xanchor="left",
                       xref="paper",
                       x=0.02))
      }else{
        method <- method%>%
          plotly::layout(
            title=list(text=base::paste0("<br><b>3x Median Discharge (w/ Uncertainty) = ",base::round(medQ,digits = 0)," + ",base::round(medQUnc,digits = 1)," = ",base::round(medQPlusUnc,digits = 0)," cfs<br>Corresponds to the dashed line plotted on the primary y-axis (left-hand; Discharge)."),
                       xanchor="left",
                       xref="paper",
                       x=0.02))
        
      }
    }else{
      if(!base::is.na(usgsProxy)){
        statMessage <- base::paste0('Due to issues with data completeness/validity, no discharge stats are currently calculated for this site.<br>For comparable data, view the <a href="',
                                    paste0("https://waterdata.usgs.gov/monitoring-location/",base::gsub("ID","",usgsProxy),"/"),
                                    '">USGS station (ID: ',base::gsub("ID","",usgsProxy),
                                    ')</a> closest in proximity to this site.<br>Use <b>',
                                    base::round(medQ,digits = 2),
                                    ' cfs </b> as 3x Median USGS Discharge.')
      }else{
        statMessage <- 'Due to issues with data completeness/validity, no discharge stats are currently calculated for this site.<br>'
      }
      method <- method%>%
        plotly::layout(
          title=list(text=statMessage,
                     xanchor="left",
                     xref="paper",
                     x=0.02))
    }
  }
  print(Sys.time())
  return(method)
}
